    private void txt_efectivoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_efectivoKeyTyped
        char c = evt.getKeyChar();

        if (!Character.isDigit(c)
                && c != '.'
                && c != java.awt.event.KeyEvent.VK_BACK_SPACE) {

            evt.consume();
            JOptionPane.showMessageDialog(this, "Solo se permiten números");
        }

        if (c == '.' && txt_efectivo.getText().contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_txt_efectivoKeyTyped

    public void cargarClientes() {

        ArchivoClienteTXT archivo = new ArchivoClienteTXT();
        List<Cliente> lista = archivo.leer();

        jComboBox_Clientes.removeAllItems();

        jComboBox_Clientes.addItem("Seleccione Cliente:");

        for (Cliente c : lista) {

            String cliente = c.getNombre() + " " + c.getApellido();

            jComboBox_Clientes.addItem(cliente);
        }
    }

    public void cargarProductos() {

        ArchivoProductoTXT archivo = new ArchivoProductoTXT();
        List<Producto> lista = archivo.leer();

        jComboBox_Productos.removeAllItems();

        jComboBox_Productos.addItem("Seleccione Producto:");

        for (Producto p : lista) {

            // Si manejas estado y quieres solo activos
            if (p.getEstado() == 1) {

                jComboBox_Productos.addItem(p.getNombre() + " (" + p.getDescripcion() + ")");
            }
        }
    }

    public void calcularTotalVenta() {

        DefaultTableModel modelo = (DefaultTableModel) jTable_Venta.getModel();

        double subtotal = 0;

        for (int i = 0; i < modelo.getRowCount(); i++) {

            subtotal += Double.parseDouble(
                    modelo.getValueAt(i, 6).toString().replace(",", "."));
        }

        double descuento = 0;

        double totalPagar = subtotal - descuento;

        jTextField_Subtotal.setText(
                String.format("%.2f", subtotal).replace(",", "."));

        jTextField_Descuento.setText(
                String.format("%.2f", descuento).replace(",", "."));

        jTextField_TotalPagar.setText(
                String.format("%.2f", totalPagar).replace(",", "."));
    }

