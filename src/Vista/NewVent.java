            titulo.setAlignment(Element.ALIGN_CENTER);

            documento.add(titulo);
            documento.add(new Paragraph(" "));

            // DATOS EMPRESA
            documento.add(new Paragraph("MiniMarket LAREDO", headerFont));
            documento.add(new Paragraph("RUC: 20512347683", textoFont));
            documento.add(new Paragraph("Telefono: 922891674", textoFont));
            documento.add(new Paragraph("Direccion: Laredo - Peru", textoFont));

            documento.add(new Paragraph(" "));

            // DATOS CLIENTE
            documento.add(new Paragraph("DATOS DEL CLIENTE", headerFont));

            documento.add(new Paragraph("Cliente: " + jComboBox_Clientes.getSelectedItem().toString(), textoFont));

            documento.add(new Paragraph("Fecha: " + fecha, textoFont));

            documento.add(new Paragraph(" "));

