    private void jTextField_BuscarDNIKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_BuscarDNIKeyTyped

        char c = evt.getKeyChar();
        // Solo números
        if (!Character.isDigit(c) && c != java.awt.event.KeyEvent.VK_BACK_SPACE) {

            evt.consume();
            JOptionPane.showMessageDialog(this, "Solo se permiten números");
        }
        // Máximo 8 dígitos
        if (jTextField_BuscarDNI.getText().length() >= 8) {
            evt.consume();
        }
        // No permitir espacios
        if (c == ' ') {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField_BuscarDNIKeyTyped

