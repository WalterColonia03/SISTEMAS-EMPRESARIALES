    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed

        String dniBuscar = jTextField_BuscarDNI.getText().trim();

        if (dniBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un DNI");
            return;
        }

        ArchivoClienteTXT archivo = new ArchivoClienteTXT();
        List<Cliente> lista = archivo.leer();

        boolean encontrado = false;

        for (Cliente c : lista) {

            if (c.getDni().equals(dniBuscar)) {

                String cliente = c.getNombre() + " " + c.getApellido();

                jComboBox_Clientes.setSelectedItem(cliente);
                jTextField_BuscarDNI.setText("");

                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado");
        }
    }//GEN-LAST:event_jButton_BuscarActionPerformed

