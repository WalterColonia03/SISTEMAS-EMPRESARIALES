    private void jButton_AñadirProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AñadirProductoActionPerformed

        String clienteSeleccionado = jComboBox_Clientes.getSelectedItem().toString();

        if (clienteSeleccionado.equals("Seleccione Cliente:")) {

            JOptionPane.showMessageDialog(this, "Seleccione un cliente");
            return;
        }

        String productoSeleccionado = jComboBox_Productos.getSelectedItem().toString();

        if (productoSeleccionado.equals("Seleccione Producto:")) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto");
            return;
        }

        if (txt_cantidad_Elegida.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una cantidad");
            return;
        }

        int cantidadElegida = Integer.parseInt(txt_cantidad_Elegida.getText());

        ArchivoProductoTXT archivo = new ArchivoProductoTXT();
        List<Producto> lista = archivo.leer();

        Producto productoEncontrado = null;

        for (Producto p : lista) {

            String nombreCombo = p.getNombre() + " (" + p.getDescripcion() + ")";

            if (nombreCombo.equals(productoSeleccionado)) {
                productoEncontrado = p;
                break;
            }
        }

        if (productoEncontrado == null) {
            JOptionPane.showMessageDialog(this, "Producto no encontrado");
            return;
        }

        // VALIDAR STOCK
        if (cantidadElegida > productoEncontrado.getCantidad()) {

            JOptionPane.showMessageDialog(this, "Stock insuficiente\nStock disponible: " + productoEncontrado.getCantidad());
            return;
        }

        double precio = productoEncontrado.getPrecio();
        double subtotal = cantidadElegida * precio;
        double descuento = 0;
        double total = subtotal - descuento;

        DefaultTableModel modelo = (DefaultTableModel) jTable_Venta.getModel();

        int numero = modelo.getRowCount() + 1;

        Object[] fila = {
            numero,
            productoEncontrado.getNombre(),
            cantidadElegida,
            String.format("%.2f", precio),
            String.format("%.2f", subtotal),
            String.format("%.2f", descuento),
            String.format("%.2f", total),
            "Eliminar"
        };

        modelo.addRow(fila);
        jComboBox_Clientes.setEnabled(false);
        calcularTotalVenta();
        txt_cantidad_Elegida.setText("");
    }//GEN-LAST:event_jButton_AñadirProductoActionPerformed

